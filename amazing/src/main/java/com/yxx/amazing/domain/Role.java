package com.yxx.amazing.domain;



public class Role {

	private Integer Id;
    /**角色名称*/
    private String roleName;
    
	public Integer getId() {
		return Id;
	}
	public void setId(Integer id) {
		Id = id;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
   
    
	
}
